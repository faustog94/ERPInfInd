    private void btn_buscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_buscarMouseClicked
        String codProducto = txt_codProducto.getText();
        if (codProducto.isEmpty()){ //no se ha colocado un cod
            JOptionPane.showMessageDialog(this, "Ingrese un c칩digo");
        }else { //Hay c칩digo
            if (helper.codigoExiste(codProducto)){ // El c칩digo existe
                prod = helper.buscarProducto(codProducto);
                lbl_descripcion.setText(prod.getDescripcion());
            }
            else { // El c칩digo no existe
                JOptionPane.showMessageDialog(this, "El c칩digo ingresado no existe");
            }
        }
    }//GEN-LAST:event_btn_buscarMouseClicked

    private void btn_a침adirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_a침adirMouseClicked
        if(list_arts.isSelectionEmpty()){
            JOptionPane.showMessageDialog(this, "Seleccione un art칤culo");
            return;
        }
        
        //A침ade a la tabla
        String descr = list_arts.getSelectedValue();
        DefaultTableModel tmodel = (DefaultTableModel)tb_artsSelec.getModel();
        tmodel.addRow(new Object[]{descr});

        //Quita de la lista
        int index = list_arts.getSelectedIndex();
        DefaultListModel<String> lmodel = (DefaultListModel) list_arts.getModel();
        lmodel.removeElementAt(index);
    }//GEN-LAST:event_btn_a침adirMouseClicked

    private void btn_quitarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_quitarMouseClicked
        if(tb_artsSelec.getSelectedRow() < 0){ //Si no hay fila seleccionada devuelve -1
            JOptionPane.showMessageDialog(this, "Seleccione un art칤culo");
            return;
        }
        
        //A침ade a la lista
        int row = tb_artsSelec.getSelectedRow();
        String descr = String.valueOf(tb_artsSelec.getValueAt(row, 0)); //La columna siempre es 0
        DefaultListModel<String> lmodel = (DefaultListModel) list_arts.getModel();
        lmodel.addElement(descr);
        //Las pr칩ximas l칤neas sirven para reordenar la lista una vez que se a침ade el elemento
        String[] array = Arrays.copyOf(lmodel.toArray(), lmodel.toArray().length, String[].class);
        Arrays.sort(array);
        list_arts.setListData(array);
        
        //Quita de la tabla
        DefaultTableModel tmodel = (DefaultTableModel)tb_artsSelec.getModel();
        tmodel.removeRow(row);

    }//GEN-LAST:event_btn_quitarMouseClicked

