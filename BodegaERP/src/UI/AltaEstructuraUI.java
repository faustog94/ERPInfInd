    private void btn_altaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_altaMouseClicked
        if(prod == null){
            JOptionPane.showMessageDialog(this, "Debe buscar un producto");
            return;
        }
        
        if(tb_artsSelec.getModel().getRowCount() == 0){
            JOptionPane.showMessageDialog(this, "Debe a침adir al menos un art칤culo");
            return;
        }
        //Chequeo que cantidad est칠 indicado para todas las filas
        DefaultTableModel model = (DefaultTableModel) tb_artsSelec.getModel();
        for (int i = 0; i < model.getRowCount(); i++) {
            if(model.getValueAt(i, 1) == null){
                JOptionPane.showMessageDialog(this, "Debe especificar cantidad para todos los art칤culos");
                return;
            }
        }
        
        String version = txt_version.getText();
        if(version.isEmpty()){
            JOptionPane.showMessageDialog(this, "Debe especificar la version");
            return;
        }
        boolean activo = chb_activo.isSelected();
        HashMap<Integer, Integer> detEst = new HashMap<Integer, Integer>();
        int idArt;
        int cantidad;
        String desc;
        for (int i = 0; i < model.getRowCount(); i++) {
            desc = (String) model.getValueAt(i, 0);
            idArt = obtenerID(desc);
            cantidad = (int) model.getValueAt(i, 1);
            detEst.put(idArt, cantidad);
        }
        
        helper.altaEstructura(prod.getIdProducto(), activo, version, detEst);
        JOptionPane.showMessageDialog(this, "Alta Satisfactoria");
    }//GEN-LAST:event_btn_altaMouseClicked
  
    private int obtenerID(String desc){
        return articulos.get(desc);
    }
